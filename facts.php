      <ul>
          <li>There is no documentation of when the first blood transfusion actually took place.</li>
        <li> References to blood transfusion can be found in religious text of most civilizations.</li>
        <li>1613- William Harvey describes the circulation of blood.</li>
        <li> Following which many speculations on the possibility of blood transfusion were made.</li>
        <li>1665- Perhaps the first documented public demonstration of blood transfusion was by Richard Lower, between two dogs. He suggested the appropriateness of blood transfusion in severe hemorrhage.</li>
        <li>1667- Jean Dennis was likely the first to perform a blood transfusion to a human being. He successfully transfused a 15 year old boy on June 15,1667.</li>
        <li>1667- Jean Dennis may have been the first to describe, what we now know as a transfusion reaction. He observed this on one of his patients, who manifested classical sighs of a hemolytic (destruction of red cells) reaction.</li>
        <li>1818- James Bundell advocated the use of human, rather than animal blood for transfusions, although only for reasons of practicality rather than on any scientific  basis.</li>
        <li>1835- The major problem faced by these early pioneers was that blood clotted within minutes of storage in a container. Bischoff proposed use of defibrinated blood to avoid clotting (the body uses fibrin as mesh to trap red blood cells and  form a clot).</li>
        <li>The modern era blood transfusion was initiated by the work of Landsteiner.</li>
        <li>1901- Blood groups (AB) were discovered by Landsteiner, who almost 40 years later discovered the second most important blood group system, the Rh system.</li>
        <li>1907- Reuben Ottenberg was the first to perform ABO typing on patients and use compatibility testing before blood transfusion.</li>
        <li>1914- By this time the need was felt for an anticoagulant that would allow blood  to be stored without clotting and citrated blood was soon to become the standard method for storing blood.</li>
        <li>1943- ACD (acid-citrate-dextrose) was introduced as an anticoagulant as it was simpler to prepare and to autoclave.</li>
        <li>1947- The first blood center was established in Chicago.</li>
        <li>1949- Plastic bags were slowly replacing glass bottles for storing blood.</li>
        <li>1970- Starting in the early 1970's only volunteer donors were accepted as blood donors. Donors giving blood for money often had other high-risk activities and  the incidence of liver diseases was high from receiving blood.</li>
        <li>1971- Testing for the hepatitis B antigen was implemented, and this together with cessation of paid donations, reduced the incidence of post-transfusion hepatitis by more than half.</li>
        <li>1980- High- risk donor deferral/ self-exclusion was initiated in the early 80s . Any donor who had a high risk behavior for infectious diseases was deferred from donating.</li>
        <li>1985- Testing for HIV was implemented.</li>
        <li>1999- The NAT testing was initiated voluntarily by America's blood centers. This test detects viral RNA/DNA and reduces the window period in which conventional tests are negative.</li>
